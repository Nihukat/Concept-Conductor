    def _encode_prompt(self,
        prompt,
        prompt_2,
        new_concept_cfg,
        device,
        num_images_per_prompt,
        do_classifier_free_guidance,
        negative_prompt=None,
        negative_prompt_2=None,
        prompt_embeds: Optional[torch.FloatTensor] = None,
        negative_prompt_embeds: Optional[torch.FloatTensor] = None,
        pooled_prompt_embeds: Optional[torch.FloatTensor] = None,
        negative_pooled_prompt_embeds: Optional[torch.FloatTensor] = None,
    ):

        assert num_images_per_prompt == 1, 'only support num_images_per_prompt=1 now'

        if prompt is not None and isinstance(prompt, str):
            batch_size = 1
        elif prompt is not None and isinstance(prompt, list):
            batch_size = len(prompt)
        else:
            batch_size = prompt_embeds.shape[0]

        if prompt_embeds is None:

            prompt_extend = bind_concept_prompt(prompt, new_concept_cfg)

            text_inputs = self.tokenizer(
                prompt_extend,
                padding='max_length',
                max_length=self.tokenizer.model_max_length,
                truncation=True,
                return_tensors='pt',
            )
            text_input_ids = text_inputs.input_ids

            # print('pos_ids:')
            # print(text_input_ids)

            prompt_embeds = self.text_encoder(text_input_ids.to(device), output_hidden_states=True)
            prompt_embeds = prompt_embeds.hidden_states[-2]
            
            if isinstance(prompt_2, str):
                prompt_2 = [prompt_2]
            n_layers = len(prompt_embeds) // batch_size
            prompt_extend_2 = [[p] * n_layers for p in prompt_2]
            prompt_extend_2 = list(itertools.chain.from_iterable(prompt_extend_2))
            
            text_inputs_2 = self.tokenizer_2(
                prompt_extend_2,
                padding='max_length',
                max_length=self.tokenizer.model_max_length,
                truncation=True,
                return_tensors='pt',
            )            
            text_input_ids_2 = text_inputs_2.input_ids
            prompt_embeds_2 = self.text_encoder_2(text_input_ids_2.to(device), output_hidden_states=True)
            
            pooled_prompt_embeds = prompt_embeds_2[0]
            n_layers = len(pooled_prompt_embeds) // batch_size
            embeds_indices = [p * n_layers for p in range(batch_size)]
            pooled_prompt_embeds = pooled_prompt_embeds[embeds_indices]
            
            
            prompt_embeds_2 = prompt_embeds_2.hidden_states[-2]
            
            prompt_embeds = torch.cat([prompt_embeds, prompt_embeds_2], dim=-1)
            
            
            
            
            
            
            
